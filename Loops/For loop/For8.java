public class For8 {
    public static void main(String[] args) {
        for(int a=2;a<=10;a++){
            System.out.println("Table of "+a+ " is :");
            for(int b=1;b<=10;b++){
                System.out.println(a*b+" ");
            }
            System.out.println();
        }
    }
    
}
